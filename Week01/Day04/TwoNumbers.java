public class TwoNumbers {
    public static void main(String[] args) {
        int a, b;
        a = 22;
        b = 13;
        int c, d, e, f, g;
        float h;
        float x = a;
        float y = b;
        c = a + b;
        d = a - b;
        e = a * b;
        f = a / b;
        g = a % b;
        h = x / y;
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(h);
        System.out.println(f);
        System.out.println(g);
    }
}
